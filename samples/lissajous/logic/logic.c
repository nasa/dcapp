void display_draw(void) {
    // Increment phase for animation
    *PHASE += 0.02;
    if (*PHASE > TWO_PI) {
        *PHASE -= TWO_PI;
    }

    // Calculate vertex positions
    for (int i = 0; i < NUM_POINTS; i++) {
        double t = (double)i / (double)(NUM_POINTS - 1) * TWO_PI;

        // Lissajous formula with animated phase
        double x = AMPLITUDE * sin(A_FREQ * t + *PHASE);
        double y = AMPLITUDE * sin(B_FREQ * t);

        // Translate to center of panel
        *x_vars[i] = CENTER_X + x;
        *y_vars[i] = CENTER_Y + y;
    }
}

